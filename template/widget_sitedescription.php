<section>
	<header>Postcard exchange</header>
	<p>
		Postcard exchange (sometimes called &ldquo;Postcrossing&rdquo; after the first website that was created to fascilitate
		this hobby) allows people to send and receive real postcards. The joy of this card is the ability to connect with a
		random person somewhere around the world, which is very important in today&apos;s world, where an e-mail is considered
		the least technical means of communicating, and many of us feel very isolated from one another.
	</p>
</section>
<section>
	<header>Happy Postcard</header>
	<p>
		This project does not seek to compete with other similar websites. We try to enhanse the ability of everybody to
		exchange postcards. Some of the unique features of this site include:
	</p>
	<ul>
		<li>send the card representing not only your country, but your particular region or the whole world,</li>
		<li>send a &laquo;Happy Birthday&raquo; card to a person without worrying about the limit,</li>
		<li>upload the image of the card as the sender and as a receiver,</li>
		<li>send the card to the person, you have received a card from,</li>
		<li>send a postcard to a person, who is in a difficult situation, and will probably not send anything back (coming soon).</li>
	</ul>
</section>
